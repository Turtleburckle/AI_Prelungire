class ColorsClass:

    def __init__(self):
        self.blue = (0, 0, 255)
        self.gray_blue = (50,120,120)
        self.red = (255, 0, 0)
        self.green = (0, 255, 0)
        self.black = (0, 0, 0)
        self.white = (255, 255, 255)

    def get_blue_color(self):
        return self.blue

    def get_gray_blue_color(self):
        return self.gray_blue

    def get_red_color(self):
        return self.red

    def get_green_color(self):
        return self.green

    def get_black_color(self):
        return self.black

    def get_white_color(self):
        return self.white