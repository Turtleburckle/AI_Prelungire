class ColorClass:
    def __init__(self):
        self.blue = (14, 71, 73)
        self.gray_blue = (50, 120, 120)
        self.red = (229, 88, 18)
        self.green = (149, 198, 35)
        self.black = (0, 38, 38)
        self.white = (239, 231, 218)

    def get_blue_color(self):
        return self.blue

    def get_gray_blue_color(self):
        return self.gray_blue

    def get_red_color(self):
        return self.red

    def get_green_color(self):
        return self.green

    def get_black_color(self):
        return self.black

    def get_white_color(self):
        return self.white
