class DirectionsClass:

    def __init__(self):
        self.__up = 0
        self.__down = 2
        self.__left = 1
        self.__right = 3

    def get_up(self):
        return self.__up

    def get_down(self):
        return self.__down

    def get_left(self):
        return self.__left

    def get_right(self):
        return self.__right