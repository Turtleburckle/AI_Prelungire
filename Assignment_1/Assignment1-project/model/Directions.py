class DirectionsClass:

    def __init__(self):
        self.up = 0
        self.down = 2
        self.left = 1
        self.right = 3

    def get_up(self):
        return self.up

    def get_down(self):
        return self.down

    def get_left(self):
        return self.left

    def get_right(self):
        return self.right
